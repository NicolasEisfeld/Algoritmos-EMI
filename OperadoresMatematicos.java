// cria a classe (.class) com o nome do arquivo ("UsandoVariaveis1")
public class OperadoresMatematicos {
// expõe a variável que vai ser declarada
 public static void main(String[] args) {
 // declara o tipo de variável das variáveis "valor" e "num" (int) e insere o valor de entrada (5, 13, respectivamente)
 int valor = 5, num = 13;
 // imprime a variável "valor" vezes a variável "num" (5 x 13)
 System.out.println( valor * num );
 // imprime a variável "valor" mais a variável "num" (5 + 13)
 System.out.println( valor + num );
 // imprime a variável "num" dividido inteiramente pela variável "num" (13 / 5)
 System.out.println( num / valor );
 // imprime o resto entre a divisão da variável "num" e "valor" (13 % 5)
 System.out.println( num % valor );
 // imprime a variável "num" menos a variável "valor" (13 - 5)
 System.out.println( num - valor );
 } 
}
// Esse programa executa com as variáveis "valor" e "num" (5, 13, respectivamente), todas as operações numéricas básicas (da linguagem java) e imprime o valor de cada resultado entre as variáveis