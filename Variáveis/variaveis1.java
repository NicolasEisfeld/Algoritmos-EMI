// cria a classe (.class) com o nome do arquivo ("UsandoVariaveis1")
public class variaveis1 {
 public static void main(String[] args) {
 // declara a variável
 int numero;
 // atribui o valor inteiro para a variável declarada
 numero = 5;
 // leitura do valor na variável
 // faz a impressão na tela
 System.out.println( numero );
 }
}
//  O programa está analisando a variável de entrada "numero" e declarando ela na classe int, (linha 5)
// após, o valor de entrada (5) é inserido na variável "numero". (linha 7)
// por último, o valor é lido na variável e é imprimido na tela usando (linha 10).
