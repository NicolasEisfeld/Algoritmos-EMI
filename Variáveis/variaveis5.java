// Cria a classe (.class) chamada "variaveis5"
public class variaveis5 {
    public static void main(String [] args) {
        // declara os tipos de variáveis (int) e os valores de entrada das variáveis "produtoquan1" e "produtoquan2"
        int produtoquan1 = 36, produtoquan2 = 27;
        // declara as variáveis "produtovalor1" e "produtovalor2" no tipo double e atribui os valores nas variáveis (159.79 e 199.99, respectivamente)
        double produtovalor1 = 159.79, produtovalor2 = 199.99;
        // declara uma variável "quanvalor1", que é o produto entre "produtoquan1" e "produtovalor1"
        double quanvalor1 = produtoquan1 * produtovalor1;
        // declara uma variável "quanvalor2", que é o produto entre "produtoquan2" e "produtovalor2"
        double quanvalor2 = produtoquan2 * produtovalor2;
        // declara uma variável "valortotal", que é a soma entre "quanvalor1" e "quanvalor2"
        double valortotal = quanvalor1 + quanvalor2;
        // imprime o valor da variável "quanvalor1"
        System.out.println(quanvalor1);
        // imprime o valor da variável "quanvalor2"
        System.out.println(quanvalor2);
        // imprime o valor da variável "valortotal"
        System.out.println(valortotal);
        
    }
}
// Nesse código, primeiramente é declarado duas quantidades de dois produtos (produtoquan1 e produtoquan2) e o valor deles (produtovalor1 e produtovalor2)
// É criado a partir dessas, duas variáveis para o valor subtotal de cada produto(quanvalor1 e quanvalor2. São os produtos entre a quantidade e o preço das variáveis produtovalor1 e produtovalor2)
// Após, é criado a variável "valortotal", que é a soma do estoque inteiro (das duas variáveis quanvalor1 e quanvalor2)
// Por fim, é imprimido na tela o valor do subtotal do produto 1, o valor do subtotal do produto 2 e o total do estoque (Isto é, as variáveis quanvalor1, quanvalor2 e valortotal, respectivamente)
