    private void botCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botCalcActionPerformed
        String texto = ctValor.getText();
        double valor = Double.parseDouble(texto);
        
        double raiz = Math.sqrt(valor);
        String result1 = String.valueOf(raiz);
        
        double cubo = valor * valor * valor;
        String result2 = String.valueOf(cubo);
        
        LResultado.setText(result1);
        LResultadoCubo.setText(result2);
        
    }//GEN-LAST:event_botCalcActionPerformed

