/*
    Escreva um algoritmo que calcule os seguintes logaritmos
*/

public class Exercicio02 {
    public static void main(String [] args) {
        System.out.println(Math.log(1000) / Math.log(2));
        System.out.println(Math.log(2187) / Math.log(3));
        System.out.println(Math.log(390625) / Math.log(5));
        System.out.println(Math.log(1.104622125) / Math.log(2));
        System.out.println(Math.log(244.140625) / Math.log(2.5));
    }
}
